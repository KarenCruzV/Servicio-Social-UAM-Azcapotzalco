    private void btn_desconectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_desconectarActionPerformed
        if(cbox_conexion.getSelectedItem().toString().compareTo("")!=0){//solo se deconesta si la salida esta conectada a algun elemento
            //System.out.println("deconectar btn " + cbox_salidas.getSelectedItem().toString());
            controlador.desconectarElemento(id,cbox_salidas.getSelectedIndex());
            JOptionPane.showMessageDialog(null, "La salida "+cbox_salidas.getSelectedItem().toString()
                                                + " del splitter se desconecto del componente: "
                                                +cbox_conexion.getSelectedItem().toString(), 
                                                "éxito", JOptionPane.INFORMATION_MESSAGE);
            setElementos(controlador.elementosDisponibles(id), "");
            
        }
    }//GEN-LAST:event_btn_desconectarActionPerformed
    
