    private void btn_aplicarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aplicarActionPerformed

        int btn_window = 1310;

        //saber que radiobutton de entradas se selecciono

        if (!rbutton_1310.isSelected()) {
            btn_window = 1550;
        }
        ///----------------------------//

        if (txtf_perdida.getText().compareTo("") == 0 || !txtf_perdida.getText().matches("[+-]?\\d*(\\.\\d+)?")) {
            JOptionPane.showMessageDialog(null, "Valor de la pérdida invalido", "error", JOptionPane.ERROR_MESSAGE);
        } else if (!validarPerdida(Double.parseDouble(txtf_perdida.getText()),cbox_nsalidas.getSelectedIndex())) {
            JOptionPane.showMessageDialog(null, "La pérdida debe ser " + buscarPerdidas(cbox_nsalidas.getSelectedIndex()), "error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (btn_aplicar.getText().compareTo("Crear") == 0) {
                controlador.crearSplitter(btn_window,cbox_nsalidas.getSelectedIndex(),
                                          Double.parseDouble(txtf_perdida.getText()));
                JOptionPane.showMessageDialog(null, "Splitter creado", "éxito", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }
            else{
                controlador.modificarSplitter(btn_window,cbox_nsalidas.getSelectedIndex(),
                                          Double.parseDouble(txtf_perdida.getText()),id);
                JOptionPane.showMessageDialog(null, "Splitter modificado", "éxito", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }
        }

    }//GEN-LAST:event_btn_aplicarActionPerformed

