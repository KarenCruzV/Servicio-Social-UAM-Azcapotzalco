    private void btn_potenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_potenciaActionPerformed
        if (txtf_sensibilidad.getText().compareTo("")==0 || !txtf_sensibilidad.getText().matches("[+-]?\\d*(\\.\\d+)?")){
            JOptionPane.showMessageDialog(null,"Valor de la sensibilidad invalido","error",JOptionPane.ERROR_MESSAGE);
         
        }
        else{
        Double potencia = controlador.calcularPotencia(id,Double.valueOf(txtf_sensibilidad.getText()));
        if(potencia !=-1)
            lbl_potencia.setText(String.valueOf(potencia + " dB"));
        else if(potencia ==-2){
            JOptionPane.showMessageDialog(null,"Error en el calculo del enlace","error",JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(null,"Error en el enlace","error",JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
        }
    }//GEN-LAST:event_btn_potenciaActionPerformed

